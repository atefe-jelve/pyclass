#calculate 2 number WHIT OPERATOR
first = float(input('please enter first number:\n'))
secend = float(input('please enter secend number:\n'))
operator = (input('please enter operator:(+ , - , * or /)\n'))
if operator == '+' :
    print(first+secend)
if operator == '-' :
    print(first-secend)
if operator == '*' :
    print(first*secend)
if operator == '/' :
    print(first/secend)
